import Foundation

extension UIColor {
    
    @objc(v_navigationAndTabBarShadowColor)
    class func navigationAndTabBarShadowColor() -> UIColor {
        return UIColor(red: 178.0 / 255.0, green: 178.0 / 255.0, blue: 178.0 / 255.0, alpha: 1.0)
    }

}
